// Smart genre categories: each maps to one or more raw `genre` values so a
// single tap (e.g. "Shooter / War") filters across FPS, TPS, Battle Royale, etc.
export interface GenreCategory {
  id: string;
  label: { en: string; ar: string };
  matches: string[]; // substrings matched (case-insensitive) against game.genre
}

export const GENRE_CATEGORIES: GenreCategory[] = [
  { id: "all", label: { en: "All Genres", ar: "كل الأنواع" }, matches: [] },
  {
    id: "shooter",
    label: { en: "Shooter / War", ar: "ألعاب حرب وشوتر" },
    matches: ["FPS", "TPS", "Shooter", "Battle Royale"],
  },
  { id: "racing", label: { en: "Racing", ar: "ألعاب سيارات" }, matches: ["Racing"] },
  { id: "sports", label: { en: "Sports", ar: "ألعاب رياضية" }, matches: ["Sports"] },
  {
    id: "rpg",
    label: { en: "RPG", ar: "ألعاب آر بي جي" },
    matches: ["RPG", "JRPG", "Souls-like", "Roguelike"],
  },
  {
    id: "action",
    label: { en: "Action", ar: "أكشن" },
    matches: ["Action", "Adventure"],
  },
];

/** Returns true when a game's genre belongs to the given category id. */
export function gameMatchesCategory(genre: string, categoryId: string): boolean {
  if (categoryId === "all") return true;
  const cat = GENRE_CATEGORIES.find((c) => c.id === categoryId);
  if (!cat || cat.matches.length === 0) return true;
  const g = genre.toLowerCase();
  return cat.matches.some((m) => g.includes(m.toLowerCase()));
}

// Sensible per-tier minimum PC requirements. Heavier genres need beefier specs.
const HEAVY_GENRES = ["RPG", "JRPG", "Souls-like", "Survival Horror", "Action-RPG", "FPS", "Battle Royale", "Looter Shooter", "Hero Shooter", "Tactical FPS"];
const MID_GENRES = ["Action", "Action-Adventure", "Adventure", "Racing", "TPS", "Shooter"];

/**
 * Returns the minimum PC system requirements for a game. Uses the explicit
 * `systemReq` field when present, otherwise derives a believable spec tier
 * from the game's genre so every PC title can show compatibility info.
 */
export function getSystemReqs(game: Game): SystemReq {
  if (game.systemReq) return game.systemReq;
  if (HEAVY_GENRES.includes(game.genre)) {
    return {
      cpu: "Intel Core i5-8400 / AMD Ryzen 5 2600",
      ram: "8 GB RAM",
      gpu: "NVIDIA GTX 1060 6GB / AMD RX 580",
    };
  }
  if (MID_GENRES.includes(game.genre)) {
    return {
      cpu: "Intel Core i5-4460 / AMD FX-8350",
      ram: "8 GB RAM",
      gpu: "NVIDIA GTX 960 / AMD R9 280",
    };
  }
  return {
    cpu: "Intel Core i3-4340 / AMD FX-6300",
    ram: "4 GB RAM",
    gpu: "NVIDIA GTX 750 Ti / AMD R7 260X",
  };
}


